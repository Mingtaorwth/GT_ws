import numpy as np

# 定义变换矩阵
T = np.array([
    [0.9999097440386976, -0.004036660078279871, -0.012814411881926828, -0.11831750882134708],
    [0.004347036737322164, 0.9996955960834941, 0.024286178021100363, -0.002210511308266823],
    [0.01271247607949047, -0.02433969076797461, 0.9996229151061157, 0.010131244666846704],
    [0.0, 0.0, 0.0, 1.0]
])

# 提取旋转矩阵 R 和位移向量 t
R = T[:3, :3]
t = T[:3, 3]

# 计算逆矩阵
R_inv = R.T  # R 的转置
t_inv = -np.dot(R_inv, t)  # 计算新的平移向量

# 构造逆矩阵
T_inv = np.eye(4)  # 初始化为 4x4 单位矩阵
T_inv[:3, :3] = R_inv
T_inv[:3, 3] = t_inv

print("原矩阵 T:")
print(T)
print("\n逆矩阵 T^-1:")
print(T_inv)
